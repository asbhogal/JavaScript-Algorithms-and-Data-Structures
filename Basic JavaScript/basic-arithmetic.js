// THE + OPERATOR ADDS TWO NUMBERS. THE = THEN ASSIGNS THE TOTAL

var sum = 10 + 0; // RETURNS 10
var sum = 10 + 10; // RETURNS 20
console.log(sum);

// THE - OPERATOR TAKES ONE FROM THE OTHER, THE = THEN ASSIGNS THE TOTAL

var difference = 45 - 0; // RETURNS 45
var difference = 45 - 30; // RETURNS 15
console.log(difference);

// THE * OPERATOR MULTIPLIES TWO ITEMS TOGETHER, THE = THEN ASSIGNS THE TOTAL

var product = 8 * 0; // RETURNS 0
var product = 8 * 10; // RETURNS 80
console.log(product);

// THE / OPERATOR DIVIDES ONE ITEM FROM THE OTHER, THE = THEN ASSIGNS THE TOTAL

var quotient = 66 / 0; // RETURNS 66
var quotient = 66 / 3; // RETURNS 22
console.log(quotient);
